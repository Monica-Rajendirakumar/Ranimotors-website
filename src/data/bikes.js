export const getBikeById = (id) => bikesData.find(b => b.id === id);

// Related bikes selector
export const getRelatedBikes = (currentBike) => {
  return bikesData
    .filter(b => b.id !== currentBike.id && (b.category === currentBike.category || Math.abs(b.price - currentBike.price) < 30000))
    .slice(0, 3);
};
